<?php

namespace App\Policies;

use App\Models\Comment;
use App\Models\User;

class CommentPolicy
{
    public function viewAny(User $user): bool
    {
        return $user->can('comments.view');
    }

    public function create(User $user): bool
    {
        return $user->can('comments.create');
    }

    public function update(User $user, Comment $comment): bool
    {
        return $user->organization_id === $comment->organization_id
            && $comment->user_id === $user->id;
    }

    public function delete(User $user, Comment $comment): bool
    {
        if ($user->organization_id !== $comment->organization_id) {
            return false;
        }

        // Users can delete their own comments, admins/managers can delete any
        return $comment->user_id === $user->id || $user->can('comments.delete');
    }
}
